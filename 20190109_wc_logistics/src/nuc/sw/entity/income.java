package nuc.sw.entity;

public class income {
	
		private int incomeId;
		private int waybillId;
		private float financeMoney;
		private String financeTime;
		private String financeName;
		public int getIncomeId() {
			return incomeId;
		}
		public void setIncomeId(int incomeId) {
			this.incomeId = incomeId;
		}
		public int getWaybillId() {
			return waybillId;
		}
		public void setWaybillId(int waybillId) {
			this.waybillId = waybillId;
		}
		public float getFinanceMoney() {
			return financeMoney;
		}
		public void setFinanceMoney(float financeMoney) {
			this.financeMoney = financeMoney;
		}
		public String getFinanceTime() {
			return financeTime;
		}
		public void setFinanceTime(String financeTime) {
			this.financeTime = financeTime;
		}
		public String getFinanceName() {
			return financeName;
		}
		public void setFinanceName(String financeName) {
			this.financeName = financeName;
		}
		
}
